# when you want to specify a type on to a variable
#int() , float(), str()

#int
x = int(2) # output 2
y = int(2.7) # output 2
z = int("2") # output 2

print("-------INT-------")
print(x)
print(y)
print(z)
print("-------INT-------\n")
#float
x = float(2) # output 2.0
y = float(2.7) # output 2.7
z = float("2") # output 2.0
z = float("2.7") # output 2.7

print("-------FLOAT-------")
print(x)
print(y)
print(z)
print("-------FLOAT-------\n")

#str
x = str(2) # output 2
y = str(2.7) # output 2
z = str("2s") # output 2s

print("-------STRING-------")
print(x)
print(y)
print(z)
print("-------STRING-------")

