# There are three number types in python - integer, float, complex
x = 4 # integer
y = 2.5 # float
z = 7j # complex

print(type(x))
print(type(y))
print(type(z))

#interger positive or negative whole number
x = 3564
y = 3527294
z = -7736484

print(type(x))
print(type(y))
print(type(z))

#float positive or negative number with decimal
x = 34.495
y = 546.394
z = -364.495

print(type(x))
print(type(y))
print(type(z))

#float can also be scientific numbers with an "e" to indicate the power of 10
x = 34e364
y = 2936E62
z = -67.4e56

print(type(x))
print(type(y))
print(type(z))

#complex numbers are written with a "j" as the imaginary part
x = 3+5j
y = 8j
z = -25j

print(type(x))
print(type(y))
print(type(z))
